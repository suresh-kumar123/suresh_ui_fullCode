/**
 * Method to add two values
 * @param {number} firstVal 
 * @param {number} secondVal 
 * @returns sum of passed values
 */
var getAddition = (firstVal, secondVal) => {
    var result = 0;
    result = firstVal + secondVal;
    return result;
}

/**
 * Method to Multiply two values
 * @param {number} firstVal 
 * @param {number} secondVal 
 * @returns sum of passed values
 */
var getMultiplication = (firstVal, secondVal) => {
    var result = 0;
    result = firstVal * secondVal;
    return result;
}

/**
 * Method to do division of two values
 * @param {number} firstVal 
 * @param {number} secondVal 
 * @returns Division of passed values
 */
var getDivision = (firstVal, secondVal) => {
    var result = 0;
    result = firstVal / secondVal;
    return result;
}

/**
 * Method to do Substraction of two values
 * @param {number} firstVal 
 * @param {number} secondVal 
 * @returns Substraction of passed values
 */
var getSubstraction = (firstVal, secondVal) => {
    var result = 0;
    result = firstVal - secondVal;
    return result;
}